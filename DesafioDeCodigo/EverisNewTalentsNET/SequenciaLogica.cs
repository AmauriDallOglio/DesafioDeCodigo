 * Desafio
Desenvolva um programa que seja capaz de ler um valor inteiro positivo N. Em seguida, o programa deverá imprimir N * 2 linhas de saída, conforme o padrão especificado abaixo. Para os valores numéricos que possuam mais de seis dígitos, todos os dígitos devem ser exibidos.

Entrada
O arquivo de entrada contém um único número inteiro positivo N (1 < N < 1000).

Saída
Para cada número i no intervalo de 1 a N, seu programa deve imprimir duas linhas. A primeira linha deve mostrar o número i, o seu quadrado (i^2), e o seu cubo (i^3). Na segunda linha, o número i deve ser seguido pelo quadrado de i mais 1, e pelo cubo de i mais 1. Siga o formato apresentado nos exemplos de saída.

Exemplo de Entrada	Exemplo de Saída
5

1 1 1
1 2 2
2 4 8
2 5 9
3 9 27
3 10 28
4 16 64
4 17 65
5 25 125
5 26 126

Observe que a sequência de saída envolve padrões aritméticos progressivos para cada número i, iniciando com o próprio i, seguido pelo cálculo do seu quadrado (i^2) e cubo (i^3). Além de variações desses valores na linha subsequente, onde é impresso i, seguido do seu quadrado + 1 e, por fim, seu cubo + 1.


